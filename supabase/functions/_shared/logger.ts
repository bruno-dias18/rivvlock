// Production-safe logger for Edge Functions
// Logs only in development to prevent information leakage in production

// TEMPORARY: Force logs in production for debugging validation deadline issue
const isDevelopment = true; // Deno.env.get('DENO_DEPLOYMENT_ID') === undefined;

export const logger = {
  log: (...args: any[]) => {
    if (isDevelopment) {
      console.log(...args);
    }
  },
  warn: (...args: any[]) => {
    if (isDevelopment) {
      console.warn(...args);
    }
  },
  error: (...args: any[]) => {
    // SECURITY: Only log errors in development to prevent exposing sensitive info
    if (isDevelopment) {
      console.error(...args);
    }
  },
  info: (...args: any[]) => {
    if (isDevelopment) {
      console.info(...args);
    }
  },
  debug: (...args: any[]) => {
    if (isDevelopment) {
      console.debug(...args);
    }
  }
};
